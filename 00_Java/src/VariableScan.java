import java.util.Scanner;

public class VariableScan {

	public static void main(String[] args) {
		// 사용자에게 입력받을 공간을 생성 
		// Scanner를 이용해서 입력받은 내용을 컴퓨터에 표시해줌
		//System.in 사용해서 키보드 입력을 읽어올 때 사용 
		//Scanner 자료형 자리에 int나 string이나 실수 boolean값을 
		//확정지어서 나타낼 수 없기 때문에 초기에는 Scanner라고 사용 
		
		//nextLine(): 문자 또는 엔터를  치기 전까지는 문장 전체를 입력 받는다
		//입력된 문자를 모두 다 가져온다.(띄어쓰기 , 특수문자 등 모두 가져옴)
		
		
		
		//Scanner a = new Scanner(System.in); // 이건 스캐너한테 a라는 이름을 부여한거임 
		Scanner a= new Scanner(System.in);
		String str=a.next();
		System.out.println(str);
		System.out.println("사용자의 이름을 입력하세요.");
		String name = a.nextLine();//이건 Sccaner라는 폴더에서 nextLine 이란걸 쓴건고 
		System.out.println(name);
		
		System.out.println("사용자의 사는 곳을 입력하세요.");
		String leave = a.nextLine();
		System.out.println(leave);
		
		System.out.println("사용자의 핸드폰 번호를 입력하세요.");
		String phoneNumber = a.nextLine();
		System.out.println(phoneNumber);
		
		System.out.println("가입한 계기를 작성하시오");
		String kaki=a.nextLine();
		System.out.println(kaki);
	
		//Scanner String을 사용해서
		// 사용자의 키 , 사용자의 나이 ,사용자의 닉네임을 입력받기 
		
		//String height , String age , String nickName
		
		 
		System.out.print("사용자의 키를 적어주세요 : ");
		String height=a.nextLine();
		System.out.println(height);
		
		System.out.print("사용자의 나이를 적어주세요 : ");
		String age=a.nextLine();
		System.out.println(age);
		
		System.out.print("사용자의 닉네임을 입력하세요 : ");
		String nickName=a.nextLine();
		System.out.println(nickName);
		
		System.out.println("다시한번 사용자의 키를 적어주세요");
		String height2 =a.next();
		System.out.println(height2);
	}

}
